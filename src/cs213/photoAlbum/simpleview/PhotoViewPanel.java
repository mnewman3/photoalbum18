    private void displayPhoto() {
        BufferedImage img = null;
        try {
            img = ImageIO.read(new File(photo.getFileName()));
        } catch (IOException e) { }
        ImageIcon icon = new ImageIcon(img.getScaledInstance(410, 265, Image.SCALE_SMOOTH));
        ImageDisplay.setIcon(icon);
        setLabels();
    }
    
    private void setLabels() {
        String caption = photo.getCaption();
        if (caption.equals(""))  caption = " ";
        photoCaption.setText(caption);
        
        if (photo.getDate() == null) {
            photoDate.setText(" ");
        } else {
            DateFormat df = new SimpleDateFormat("MM/dd/yyyy-HH:mm:ss");
            String date = df.format(photo.getDate().getTime());
            photoDate.setText(date);
        }
        
        String tags = photo.getTagList().toString();
        if (tags.equals("") || tags.equals("[]")) {
            tags = " ";
        } else {
            tags = tags.substring(1, tags.length()-1);
        }
        photoTagList.setText(tags);
        photoTagList.setToolTipText(tags);
    }
    
    
