    private void btnPrevActionPerformed(java.awt.event.ActionEvent evt) {
        if(index == 0) {
            index = photoList.size()-1;
//            return;
        } else {
            index --;
        }
        photo = photoList.get(index);
        displayPhoto();
    }
    
    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {
        if (photoList.size() <= index+1) {
            index = 0;
//            return;
        } else {
            index++;
        }
        photo = photoList.get(index);
        displayPhoto();
    }

    private void btnDeleteTagActionPerformed(java.awt.event.ActionEvent evt) {
        
        LinkedList<Tag> tagList = photo.getTagList();
        for(Tag tag : tagList) {
            ddlTags.addItem(tag);
        }
        DeleteTagDialog.setVisible(true);
    }

    private void btnAddTagActionPerformed(java.awt.event.ActionEvent evt) {
        AddTagDialog.setVisible(true);
    }

    private void btnAddTagCancelActionPerformed(java.awt.event.ActionEvent evt) {
        AddTagDialog.setVisible(false);
        TagTypeField.setText("");
        TagValueField.setText("");
    }

    private void btnAddTagOkActionPerformed(java.awt.event.ActionEvent evt) {
        
        try {
            control.addTag(photo.getFileName(), TagTypeField.getText(), TagValueField.getText());
        } catch (Exception e) {
            if (e instanceof PhotoException) {
                AddTagErrorLabel.setText("Error: Invalid photo");
            } else {
                AddTagErrorLabel.setText("Error: Tag already exists for photo");
            }
            return;
        }
        
        AddTagDialog.setVisible(false);
        TagTypeField.setText("");
        TagValueField.setText("");
        setLabels();
    }

    private void btnDeleteTagOkActionPerformed(java.awt.event.ActionEvent evt) {
        
        // delete tag stuff
        Tag tag = (Tag)ddlTags.getSelectedItem();
        try {
            control.deleteTag(photo.getFileName(), tag.getType(), tag.getValue());
        } catch (Exception e) {
             if (e instanceof PhotoException) {
                DeleteTagErrorLabel.setText("Error: Invalid photo");
            } else {
                DeleteTagErrorLabel.setText("Error: Tag doesn't exist");
            }
            return;
        }
        ddlTags.removeAllItems();
        DeleteTagDialog.setVisible(false);
        setLabels();
    }

    private void btnDeleteTagCancelActionPerformed(java.awt.event.ActionEvent evt) {
        ddlTags.removeAllItems();
        DeleteTagDialog.setVisible(false);
    }

